# В этом файле находится логика проверки строки на палиндром.

# Алгоритм проверки строки на палиндром по шагам:
# 0. Получаем исходную строку от пользователя.
# 1. Переводим все буквы строки в нижний регистр, чтобы не зависеть от больших и маленьких букв.
# 2. Убираем все символы, которые не являются буквами или цифрами.
# 3. Получаем очищенную строку, с которой уже удобно работать.
# 4. Создаем левый указатель и ставим его в начало строки.
# 5. Создаем правый указатель и ставим его в конец строки.
# 6. Сравниваем символы слева и справа.
# 7. Если хотя бы одна пара символов не совпала, значит строка не является палиндромом.
# 8. Если символы совпали, сдвигаем левый указатель вправо, а правый указатель влево.
# 9. Повторяем проверку, пока указатели не встретятся или не пересекутся.
# 10. Если все пары символов совпали, значит строка является палиндромом.

# Создаем функцию для очистки строки от лишних символов.
def normalize_text(text: str) -> str:
    # Создаем пустой список, в который будем складывать только нужные символы.
    normalized_characters = []
    # Переводим всю строку в нижний регистр, чтобы сравнение было правильным.
    lowercase_text = text.lower()
    # Перебираем строку посимвольно.
    for symbol in lowercase_text:
        # Проверяем, является ли символ буквой или цифрой.
        if symbol.isalnum():
            # Добавляем подходящий символ в список очищенных символов.
            normalized_characters.append(symbol)
    # Собираем итоговую строку из списка символов и возвращаем ее.
    return "".join(normalized_characters)

# Создаем функцию для проверки, является ли строка палиндромом.
def is_palindrome(text: str) -> bool:
    # Получаем очищенную строку без пробелов, знаков препинания и лишних символов.
    normalized_text = normalize_text(text)
    # Проверяем, остались ли в строке буквы или цифры после очистки.
    if normalized_text == "":
        # Возвращаем False, потому что пустую строку в рамках этого учебного проекта не считаем палиндромом.
        return False
    # Создаем левый указатель и ставим его на первый символ строки.
    left_index = 0
    # Создаем правый указатель и ставим его на последний символ строки.
    right_index = len(normalized_text) - 1
    # Запускаем цикл, пока левый указатель не дошел до правого.
    while left_index < right_index:
        # Сравниваем символ слева и символ справа.
        if normalized_text[left_index] != normalized_text[right_index]:
            # Сразу возвращаем False, если нашли хотя бы одно несовпадение.
            return False
        # Сдвигаем левый указатель на один символ вправо.
        left_index += 1
        # Сдвигаем правый указатель на один символ влево.
        right_index -= 1
    # Возвращаем True, если все пары символов совпали.
    return True